// ============================================================
// ЗАДАЧА: Удаление смайликов из строки
// ============================================================
// Удалить из строки все вхождения смайликов вида ":-)" и ":-(").
//
// Примеры:
//   removeSmileys("hello :-)world")     → "hello world"
//   removeSmileys(":-) :-(")            → " "
//   removeSmileys("no smileys here")    → "no smileys here"
//   removeSmileys(":-):-(")             → ""
// ============================================================

String removeSmileys(String s) {
  // Подход 1: Конечный автомат (один проход, O(1) памяти)
  // Состояния: 0=обычный, 1=видели ':', 2=видели ':' затем '-'
  final result = StringBuffer();
  int state = 0; // текущее состояние автомата

  for (int i = 0; i < s.length; i++) {
    final c = s[i];

    if (state == 0) {
      // Обычное состояние
      if (c == ':') {
        state = 1; // видели ':' — ждём '-'
        // НЕ добавляем ':' ещё — возможно это начало смайлика
      } else {
        result.write(c); // обычный символ — добавляем
      }
    } else if (state == 1) {
      // Видели ':'
      if (c == '-') {
        state = 2; // видели ':-' — ждём ')' или '('
      } else if (c == ':') {
        result.write(':'); // ':' + ':' → первый ':' не смайлик, выводим его
        state = 1;          // новый ':' — снова ждём '-'
      } else {
        result.write(':'); // ':' не продолжилось → выводим сохранённый ':'
        result.write(c);   // и текущий символ
        state = 0;
      }
    } else if (state == 2) {
      // Видели ':-'
      if (c == ')' || c == '(') {
        state = 0; // нашли смайлик ':-)'  или ':-(' — пропускаем, сбрасываем
      } else if (c == ':') {
        result.write(':');  // ':' + '-' + ':' → выводим ':-' и начинаем новый
        result.write('-');
        state = 1;
      } else {
        result.write(':');  // ':-' не продолжилось → выводим ':-'
        result.write('-');
        result.write(c);
        state = 0;
      }
    }
  }

  // Если в конце остались незавершённые состояния — выводим накопленное
  if (state == 1) result.write(':');
  if (state == 2) {
    result.write(':');
    result.write('-');
  }

  return result.toString();
}

// Подход 2: Через replaceAll (короче, но зависит от библиотеки)
String removeSmileysRegex(String s) {
  return s.replaceAll(RegExp(r':-[)(]'), '');
}
