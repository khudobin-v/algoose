// ============================================================
// ЗАДАЧА: Фильтрация отсортированного массива по списку исключений
// ============================================================
// Даны два отсортированных массива a и exclude.
// Вернуть элементы a, которых нет в exclude.
//
// Примеры:
//   filter([1,2,3,4,5], [2,4])   → [1,3,5]
//   filter([1,1,2,3], [1,3])     → [2]  (оба вхождения 1 убираем)
//   filter([1,2,3], [])          → [1,2,3]
//   filter([1,2,3], [1,2,3,4])   → []
// ============================================================

List<int> filterByList(List<int> a, List<int> exclude) {
  final result = <int>[];

  int i = 0; // указатель по массиву a
  int j = 0; // указатель по массиву exclude

  while (i < a.length) {
    // Двигаем j вперёд пока exclude[j] < a[i]
    // Все элементы exclude, меньшие a[i], нам не интересны
    while (j < exclude.length && exclude[j] < a[i]) {
      j++;
    }

    // Теперь либо j вышел за границу (значит a[i] нет в exclude)
    // либо exclude[j] >= a[i]
    if (j >= exclude.length || exclude[j] != a[i]) {
      // a[i] не найден в exclude → включаем в результат
      result.add(a[i]);
    }
    // Если exclude[j] == a[i] → пропускаем a[i]

    i++; // переходим к следующему элементу a
  }

  return result;
}
