// ============================================================
// ЗАДАЧА: Найти пару эквивалентных поддеревьев
// ============================================================
// В бинарном дереве найти два поддерева с одинаковой структурой
// и одинаковыми значениями узлов.
//
// Примеры:
//   Дерево: 1→(2→(4,5), 3→(2→(4,5),6))
//   Поддерево 2→(4,5) встречается дважды → возвращаем его
// ============================================================

class TreeNode {
  int val;
  TreeNode? left;
  TreeNode? right;
  TreeNode(this.val, [this.left, this.right]);
}

// Подход: сериализовать каждое поддерево в строку,
// найти дублирующиеся строки через HashMap
List<TreeNode> findDuplicateSubtrees(TreeNode? root) {
  final count = <String, int>{}; // строка-дескриптор → количество вхождений
  final result = <TreeNode>[];   // найденные дубликаты

  // Рекурсивно строим дескриптор каждого поддерева
  _serialize(root, count, result);

  return result;
}

// Сериализует поддерево в строку и обновляет счётчики
// Формат: "значение,левое_поддерево,правое_поддерево"
// Пустой узел обозначается "#"
String _serialize(TreeNode? node, Map<String, int> count, List<TreeNode> result) {
  // Базовый случай: пустой узел
  if (node == null) return '#';

  // Рекурсивно сериализуем левое и правое поддерево
  final left = _serialize(node.left, count, result);
  final right = _serialize(node.right, count, result);

  // Дескриптор текущего поддерева — уникально описывает его структуру и значения
  final descriptor = '${node.val},$left,$right';

  // Обновляем счётчик для этого дескриптора
  count[descriptor] = (count[descriptor] ?? 0) + 1;

  // Если встретили ровно второй раз — добавляем в результат
  // (не третий раз, чтобы не дублировать в результате)
  if (count[descriptor] == 2) {
    result.add(node);
  }

  return descriptor; // возвращаем дескриптор вызывающему
}
