// ============================================================
// ЗАДАЧА: Сложение двух hex-чисел, заданных строками
// ============================================================
// Сложить два шестнадцатеричных числа, заданных строками.
// Вернуть результат в виде строки в hex-формате.
// Работает как сложение столбиком с переносом.
//
// Примеры:
//   addHex("1a", "2b")   → "45"   (26+43=69=0x45)
//   addHex("ff", "1")    → "100"  (255+1=256=0x100)
//   addHex("0", "0")     → "0"
//   addHex("abc", "def") → "19ab" (2748+3567=6315=0x18AB)
// ============================================================

String addHex(String a, String b) {
  // Строки цифр: идём с конца (младший разряд последний)
  int i = a.length - 1; // указатель по a
  int j = b.length - 1; // указатель по b
  int carry = 0;         // перенос в следующий разряд

  final result = StringBuffer();

  // Идём пока есть цифры в a, b или есть перенос
  while (i >= 0 || j >= 0 || carry > 0) {
    // Берём цифры — если указатель вышел за границу, считаем цифру = 0
    final digitA = i >= 0 ? _hexDigitToInt(a[i]) : 0;
    final digitB = j >= 0 ? _hexDigitToInt(b[j]) : 0;

    // Складываем цифры + перенос
    final sum = digitA + digitB + carry;
    carry = sum ~/ 16; // целочисленное деление на 16 — новый перенос

    // Остаток — цифра результата (добавляем в начало, т.к. строим с конца)
    result.write(_intToHexDigit(sum % 16));

    i--; // двигаем указатели
    j--;
  }

  // Если результат пустой (оба нуля и carry=0) — возвращаем "0"
  if (result.isEmpty) return '0';

  // Переворачиваем — мы строили цифры от младшего к старшему
  return result.toString().split('').reversed.join();
}

// Конвертирует символ hex-цифры в число (0-15)
int _hexDigitToInt(String c) {
  final code = c.toLowerCase().codeUnitAt(0);
  if (code >= 48 && code <= 57) return code - 48;       // '0'-'9'
  return code - 97 + 10;                                  // 'a'-'f' → 10-15
}

// Конвертирует число (0-15) в символ hex-цифры
String _intToHexDigit(int n) {
  if (n < 10) return String.fromCharCode(48 + n);        // 0-9
  return String.fromCharCode(97 + n - 10);               // 10-15 → 'a'-'f'
}
