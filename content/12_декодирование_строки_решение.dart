// ============================================================
// РЕШЕНИЕ: Разворачивание скобочной грамматики (рекурсия)
// ============================================================

String expandGrammar(String s) {
  // Запускаем рекурсивный парсер с позиции 0
  // Он вернёт (результат, финальная позиция) — позиция нам здесь не нужна
  final (result, _) = _parse(s, 0);
  return result;
}

// Парсит строку начиная с позиции i, останавливается на ']' или конце строки
// Возвращает: (собранная строка, позиция где остановились)
(String, int) _parse(String s, int i) {
  final result = StringBuffer();

  // Идём вперёд пока не встретим ']' или конец строки
  // ']' означает что мы внутри скобок и нужно вернуться наверх
  while (i < s.length && s[i] != ']') {

    if (s[i] == '[') {
      // Нашли открывающую скобку — рекурсивно разбираем содержимое
      // Передаём i+1 чтобы пропустить сам '['
      final (inner, newI) = _parse(s, i + 1);

      // newI указывает на ']' — пропускаем его (+1)
      i = newI + 1;

      // После ']' идёт число — читаем его (может быть многозначным: [a12])
      int count = 0;
      while (i < s.length && _isDigit(s[i])) {
        count = count * 10 + int.parse(s[i]);
        i++;
      }
      // Если число не указано — повторяем 1 раз
      if (count == 0) count = 1;

      // Записываем inner повторённый count раз
      for (int k = 0; k < count; k++) {
        result.write(inner);
      }

    } else {
      // Обычная буква — просто добавляем и двигаемся вперёд
      result.write(s[i]);
      i++;
    }
  }

  // Возвращаем результат и позицию где остановились
  // Если остановились на ']' — вызывающая функция пропустит его сама
  return (result.toString(), i);
}

bool _isDigit(String c) {
  final code = c.codeUnitAt(0);
  return code >= 48 && code <= 57; // '0'=48, '9'=57
}
