// ============================================================
// ЗАДАЧА: URL-кодирование строки (замена пробелов на %20)
// ============================================================
// Заменить все пробелы в строке на "%20".
// Реализовать in-place: два прохода — сначала считаем пробелы,
// потом идём с конца, расширяя строку.
//
// Примеры:
//   urlEncode("Mr John Smith") → "Mr%20John%20Smith"
//   urlEncode("hello world")   → "hello%20world"
//   urlEncode("nospace")       → "nospace"
// ============================================================

String urlEncode(String s) {
  // В Dart строки иммутабельны, поэтому симулируем in-place через List<String>
  // В реальном собесе (C++/Java) это делается на массиве символов

  // ШАГ 1: Считаем количество пробелов
  int spaceCount = 0;
  for (int i = 0; i < s.length; i++) {
    if (s[i] == ' ') spaceCount++;
  }

  // Если пробелов нет — возвращаем как есть
  if (spaceCount == 0) return s;

  // ШАГ 2: Создаём массив нужного размера
  // Каждый пробел заменяется на 3 символа ('%','2','0') — увеличение на 2
  final newLength = s.length + spaceCount * 2;
  final result = List<String>.filled(newLength, '');

  // ШАГ 3: Идём с КОНЦА строки, заполняем с конца нового массива
  // Это позволяет не затирать ещё не обработанные символы
  int writePos = newLength - 1; // указатель записи — начинаем с конца

  for (int i = s.length - 1; i >= 0; i--) {
    if (s[i] == ' ') {
      // Пробел → записываем '%20' с конца (в обратном порядке символов)
      result[writePos] = '0';     // последний символ '%20'
      result[writePos - 1] = '2'; // средний символ
      result[writePos - 2] = '%'; // первый символ
      writePos -= 3;              // сдвигаем указатель на 3 позиции влево
    } else {
      // Обычный символ — просто копируем
      result[writePos] = s[i];
      writePos--; // сдвигаем указатель на 1 позицию влево
    }
  }

  return result.join();
}
