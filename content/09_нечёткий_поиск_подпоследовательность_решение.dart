// ============================================================
// ЗАДАЧА: Нечёткий поиск — проверка подпоследовательности
// ============================================================
// Проверить, является ли строка s подпоследовательностью строки t.
// Символы s должны встречаться в t в том же порядке, но не обязательно подряд.
//
// Примеры:
//   isSubsequence("ace", "abcde") → true  ("a","c","e" встречаются по порядку)
//   isSubsequence("aec", "abcde") → false ("e" стоит раньше "c" в t)
//   isSubsequence("", "abc")      → true  (пустая строка — подпоследовательность любой)
//   isSubsequence("abc", "")      → false (s непустая, t пустая)
// ============================================================

bool isSubsequence(String s, String t) {
  // Указатель по строке s — показывает, какой символ ищем сейчас
  int i = 0;
  // Указатель по строке t — проходим всю строку t
  int j = 0;

  // Идём по t, пока не закончится t или не найдём все символы s
  while (i < s.length && j < t.length) {
    // Если текущий символ t совпадает с текущим символом s —
    // продвигаем указатель по s (нашли очередной нужный символ)
    if (s[i] == t[j]) {
      i++; // символ s[i] найден, переходим к следующему символу s
    }
    // Указатель по t двигаем всегда — просматриваем каждый символ t
    j++;
  }

  // Если i дошёл до конца s — все символы s найдены в t по порядку
  // Если i не дошёл — не все символы s встретились
  return i == s.length;
}

// ============================================================
// ДОПОЛНИТЕЛЬНО: найти индексы совпадений в t
// ============================================================

List<int>? findSubsequenceIndices(String s, String t) {
  // Возвращает список индексов в t, где найдены символы s,
  // или null если s не является подпоследовательностью t

  final indices = <int>[]; // индексы совпадений в t
  int i = 0; // указатель по s

  for (int j = 0; j < t.length; j++) {
    // Если текущий символ t совпадает — запоминаем его индекс
    if (i < s.length && s[i] == t[j]) {
      indices.add(j); // запоминаем позицию в t
      i++; // переходим к следующему символу s
    }
  }

  // Возвращаем индексы если нашли все символы s, иначе null
  return i == s.length ? indices : null;
}
