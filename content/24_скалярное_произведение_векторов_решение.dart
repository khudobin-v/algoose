// ============================================================
// ЗАДАЧА: Скалярное произведение разреженных векторов
// ============================================================
// Векторы заданы как список пар (value, count) — run-length encoding.
// Найти скалярное произведение двух таких векторов.
//
// Пример:
//   a = [(3,2),(0,1),(5,1)] означает [3,3,0,5] (3 дважды, 0 один раз, 5 один раз)
//   b = [(2,1),(0,2),(4,1)] означает [2,0,0,4]
//   Скалярное произведение [3,3,0,5] · [2,0,0,4] = 3*2 + 3*0 + 0*0 + 5*4 = 26
// ============================================================

// Пара (значение, количество повторений)
typedef RunPair = (int value, int count);

int dotProduct(List<RunPair> a, List<RunPair> b) {
  int result = 0;

  int i = 0; // индекс текущего run в a
  int j = 0; // индекс текущего run в b

  int remA = 0; // сколько элементов осталось в текущем run a
  int remB = 0; // сколько элементов осталось в текущем run b

  // Инициализируем первые runs
  if (a.isNotEmpty) remA = a[0].$2;
  if (b.isNotEmpty) remB = b[0].$2;

  // Идём одновременно по обоим векторам
  while (i < a.length && j < b.length) {
    // Количество элементов которые можно обработать за этот шаг —
    // минимум из остатков текущих runs
    final step = remA < remB ? remA : remB;

    // Добавляем вклад в скалярное произведение:
    // step элементов с одинаковыми значениями a[i].value и b[j].value
    result += a[i].$1 * b[j].$1 * step;

    // Уменьшаем остатки
    remA -= step;
    remB -= step;

    // Если run a исчерпан — переходим к следующему run
    if (remA == 0) {
      i++;
      if (i < a.length) remA = a[i].$2; // инициализируем новый run
    }

    // Если run b исчерпан — переходим к следующему run
    if (remB == 0) {
      j++;
      if (j < b.length) remB = b[j].$2;
    }
  }

  return result;
}
