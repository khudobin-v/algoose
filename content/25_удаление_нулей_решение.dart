// ============================================================
// ЗАДАЧА: Удалить нули из массива, сохранив порядок остальных
// ============================================================
// Удалить все нули из массива in-place, сохранив порядок ненулевых элементов.
// O(n) время, O(1) память.
//
// Примеры:
//   removeZeros([0,1,0,3,12]) → [1,3,12,0,0]  (нули в конце)
//   removeZeros([1,0,0,2,3])  → [1,2,3,0,0]
//   removeZeros([0,0,0])      → [0,0,0]
//   removeZeros([1,2,3])      → [1,2,3]
// ============================================================

// Вариант 1: Перемещаем ненулевые элементы вперёд
void removeZeros(List<int> nums) {
  // writePos — позиция куда пишем следующий ненулевой элемент
  int writePos = 0;

  // Проход 1: переносим все ненулевые элементы в начало
  for (int i = 0; i < nums.length; i++) {
    if (nums[i] != 0) {
      // Ненулевой элемент — записываем на позицию writePos
      nums[writePos] = nums[i];
      writePos++; // сдвигаем позицию записи
    }
    // Нулевые элементы просто пропускаем
  }

  // Проход 2: заполняем оставшиеся позиции нулями
  while (writePos < nums.length) {
    nums[writePos] = 0;
    writePos++;
  }
}

// Вариант 2: swap — меняем ноль и ненулевой элемент местами
// Сохраняет относительный порядок ненулевых элементов
void removeZerosSwap(List<int> nums) {
  int writePos = 0; // указатель на первый ноль

  for (int i = 0; i < nums.length; i++) {
    if (nums[i] != 0) {
      // Меняем местами nums[i] (ненулевой) и nums[writePos] (ноль или тот же элемент)
      final temp = nums[writePos];
      nums[writePos] = nums[i];
      nums[i] = temp;
      writePos++; // следующий ноль правее
    }
  }
}
