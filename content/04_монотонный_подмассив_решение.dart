// ============================================================
// РЕШЕНИЕ: Максимальный монотонный подотрезок
// ============================================================

(int, int) longestMonotone(List<int> a) {
  // Массив из 0 или 1 элемента — любой одиночный элемент является ответом
  if (a.length <= 1) return (0, a.length - 1);

  // Лучший найденный отрезок (начало и конец включительно)
  int bestStart = 0;
  int bestEnd = 0;

  // Начало текущего отрезка
  int start = 0;

  // Направление текущего отрезка: 1 = возрастание, -1 = убывание, 0 = не определено
  int direction = 0;

  // Идём с i=1, сравниваем a[i] с a[i-1]
  for (int i = 1; i < a.length; i++) {
    int? curDir;

    if (a[i] > a[i - 1]) {
      curDir = 1;   // возрастание
    } else if (a[i] < a[i - 1]) {
      curDir = -1;  // убывание
    } else {
      // Равные элементы — строгой монотонности нет
      // Начинаем новый отрезок с текущего элемента
      // direction сбрасываем в 0 — следующий шаг определит направление заново
      start = i;
      direction = 0;
      continue; // переходим к следующей итерации
    }

    // Направление сменилось (вверх→вниз или вниз→вверх)
    // Новый отрезок начинается с ПРЕДЫДУЩЕГО элемента (i-1)
    // Почему i-1, а не i? Потому что a[i-1] входит в новый отрезок как его начало
    // Пример: [1,2,1] — на i=2 видим спуск, новый отрезок: [2,1], start=1=i-1
    if (direction != 0 && curDir != direction) {
      start = i - 1;
    }

    // Обновляем направление
    direction = curDir;

    // Проверяем: текущий отрезок [start..i] длиннее лучшего [bestStart..bestEnd]?
    // Сравниваем длины через разницу индексов (не +1, т.к. оба сравнения одинаковы)
    if (i - start > bestEnd - bestStart) {
      bestStart = start;
      bestEnd = i;
    }
  }

  return (bestStart, bestEnd);
}
