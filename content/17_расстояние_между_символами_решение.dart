// ============================================================
// ЗАДАЧА: Минимальное расстояние между X и Y
// ============================================================
// Дана строка из символов 'X', 'Y', 'O'.
// Найти минимальное расстояние между любым X и любым Y в строке.
// Расстояние = разность индексов по абсолютной величине.
//
// Примеры:
//   minDistance("XOOYOX")  → 1  (X на 4, Y на 3 — расстояние 1... нет Y! пример плохой)
//   minDistance("XOOYOYX") → 1  (Y на 5, X на 6)
//   minDistance("XY")      → 1
//   minDistance("XOOOY")   → 4
// ============================================================

int? minDistanceXY(String s) {
  // Если нет хотя бы одного X и одного Y — нет ответа
  if (!s.contains('X') || !s.contains('Y')) return null;

  int? minDist; // лучший найденный результат

  // Последняя позиция X и Y которые мы видели
  int? lastX; // индекс последнего встреченного X
  int? lastY; // индекс последнего встреченного Y

  // Один проход слева направо
  for (int i = 0; i < s.length; i++) {
    if (s[i] == 'X') {
      lastX = i; // обновляем позицию последнего X
      // Если перед этим видели Y — считаем расстояние
      if (lastY != null) {
        final dist = (lastX - lastY).abs();
        // Обновляем минимум
        if (minDist == null || dist < minDist) minDist = dist;
      }
    } else if (s[i] == 'Y') {
      lastY = i; // обновляем позицию последнего Y
      // Если перед этим видели X — считаем расстояние
      if (lastX != null) {
        final dist = (lastY - lastX).abs();
        // Обновляем минимум
        if (minDist == null || dist < minDist) minDist = dist;
      }
    }
    // 'O' — пропускаем
  }

  return minDist;
}
