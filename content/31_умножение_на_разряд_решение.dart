// ============================================================
// ЗАДАЧА: Умножить большое число на однозначный множитель
// ============================================================
// Большое число задаётся строкой цифр, МЛАДШИЙ разряд ПЕРВЫЙ.
// Умножить на число от 1 до 9. Результат в том же формате.
//
// Примеры:
//   multiply("321", 2)  → "246"   (123 * 2 = 246, обе строки в порядке "мл первый")
//   multiply("1", 9)    → "9"
//   multiply("99", 9)   → "198"   (99 * 9 = 891, в формате = "198"... нет)
//   Пояснение: "99" = 99 (9 в позиции 0 = единицы, 9 в позиции 1 = десятки)
//              99 * 9 = 891. В формате мл-первый: "198"
// ============================================================

String multiplyByDigit(String number, int digit) {
  // Если множитель 0 — результат всегда "0"
  if (digit == 0) return '0';

  final result = StringBuffer();
  int carry = 0; // перенос в следующий разряд

  // Идём по числу слева направо (т.к. мл. разряд первый — это уже нужный порядок)
  for (int i = 0; i < number.length; i++) {
    // Текущая цифра числа (число[i] — это цифра i-го разряда)
    final d = number.codeUnitAt(i) - 48; // '0'=48

    // Произведение текущей цифры на множитель плюс перенос
    final product = d * digit + carry;

    // Текущая цифра результата — остаток от деления на 10
    result.write(product % 10);

    // Новый перенос — целая часть от деления на 10
    carry = product ~/ 10;
  }

  // Если после обработки всех цифр остался перенос — добавляем его
  // Перенос может быть многозначным при умножении на 9
  while (carry > 0) {
    result.write(carry % 10); // добавляем цифры переноса
    carry ~/= 10;
  }

  return result.toString();
}
