// ============================================================
// РЕШЕНИЕ: Одно исправление между строками
// ============================================================

bool isOneEditAway(String s, String t) {
  // Если разница длин больше 1 — нужно минимум 2 операции → сразу false
  if ((s.length - t.length).abs() > 1) return false;

  // Гарантируем что s — более короткая (или равная) строка
  // Это упрощает логику: нам всегда нужно двигать указатель длинной строки при пропуске
  if (s.length > t.length) return isOneEditAway(t, s);

  // Теперь s.length <= t.length
  int i = 0; // указатель по короткой строке s
  int j = 0; // указатель по длинной (или равной) строке t
  bool usedEdit = false; // использовали ли уже одно исправление

  while (i < s.length && j < t.length) {
    if (s[i] != t[j]) {
      // Символы не совпали — это исправление
      // Если исправление уже было — нужно второе → false
      if (usedEdit) return false;
      usedEdit = true;

      if (s.length == t.length) {
        // Строки одинаковой длины → операция ЗАМЕНА
        // Двигаем оба указателя: заменяем s[i] на t[j] и идём дальше
        i++;
        j++;
      } else {
        // Строки разной длины → операция ВСТАВКА/УДАЛЕНИЕ
        // Двигаем только указатель ДЛИННОЙ строки — как будто "пропускаем" лишний символ
        j++;
        // i остаётся на месте — следующая итерация снова сравнит s[i] с t[j+1]
      }
    } else {
      // Символы совпали — оба указателя вперёд
      i++;
      j++;
    }
  }

  // Если мы дошли до конца — всё ок
  // Хвост в t (если он есть) — это ровно одна операция вставки, допустимо
  return true;
}
