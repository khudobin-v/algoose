// ============================================================
// РЕШЕНИЕ: Количество подстрок без повторяющихся символов
// ============================================================

int countUniqueSubstrings(String s) {
  if (s.isEmpty) return 0;

  // Скользящее окно [left..j]
  // left — левая граница окна без повторов
  int left = 0;

  // Храним последнюю позицию каждого символа
  // Ключ: символ, Значение: последний индекс где он встречался
  final lastSeen = <String, int>{};

  int result = 0;

  // j — правая граница окна, двигаем вправо по одному
  for (int j = 0; j < s.length; j++) {
    final char = s[j];

    // Если символ уже был в ТЕКУЩЕМ окне (lastSeen[char] >= left)
    // — нужно сдвинуть левую границу вправо от предыдущего вхождения
    // Важно: >= left, а не просто contains! Символ мог быть ДО текущего окна
    if (lastSeen.containsKey(char) && lastSeen[char]! >= left) {
      left = lastSeen[char]! + 1;
      // Пример: "abca", j=3 (char='a'), lastSeen['a']=0
      // left становится 1 — окно теперь "bca"
    }

    // Обновляем последнюю позицию символа
    lastSeen[char] = j;

    // Сколько валидных подстрок заканчивается на позиции j?
    // Это все подстроки с левым концом от left до j включительно
    // Их ровно j - left + 1 штук
    result += j - left + 1;
  }

  return result;
}
