// ============================================================
// ЗАДАЧА: Всплески активности — события за последние 5 минут
// ============================================================
// Дана последовательность событий с временными метками (в секундах).
// Для каждого события найти количество событий (включая текущее)
// за последние 5 минут (300 секунд).
//
// Примеры:
//   events=[10,60,120,300,310] → [1,2,3,4,4]
//   events=[0,100,200,300,400] → [1,2,3,4,4]  (0+300=300, событие в 300 входит)
// ============================================================

import 'dart:collection';

List<int> eventsInLastFiveMinutes(List<int> timestamps) {
  final result = <int>[];

  // Используем очередь (deque) для хранения временных меток в окне
  // В очереди всегда только те события, которые попадают в окно [t-300, t]
  final window = Queue<int>();

  for (final t in timestamps) {
    // Убираем из очереди события старше 5 минут
    // Событие выходит из окна если: t - timestamp > 300
    while (window.isNotEmpty && t - window.first > 300) {
      window.removeFirst(); // удаляем устаревшее событие из начала очереди
    }

    // Добавляем текущее событие в конец окна
    window.addLast(t);

    // Количество событий в окне = размер очереди
    result.add(window.length);
  }

  return result;
}
