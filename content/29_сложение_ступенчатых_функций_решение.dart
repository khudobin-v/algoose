// ============================================================
// ЗАДАЧА: Сложение двух ступенчатых функций
// ============================================================
// Ступенчатая функция задаётся списком точек излома (x, y).
// Функция равна y начиная с x и до следующей точки излома.
// Сложить две такие функции в одну.
//
// Пример:
//   f = [(0,1),(3,2),(7,0)] означает:
//     f(x) = 1 при x in [0,3)
//     f(x) = 2 при x in [3,7)
//     f(x) = 0 при x >= 7
//
//   g = [(0,2),(5,1)]
//     g(x) = 2 при x in [0,5)
//     g(x) = 1 при x >= 5
//
//   (f+g)(x) = [(0,3),(3,4),(5,3),(7,1)]
// ============================================================

typedef Point = (int x, int y); // точка излома

List<Point> addStepFunctions(List<Point> f, List<Point> g) {
  final result = <Point>[];

  int i = 0; // указатель по f
  int j = 0; // указатель по g

  int fVal = 0; // текущее значение f (до первой точки = 0)
  int gVal = 0; // текущее значение g

  // Сливаем два отсортированных списка точек излома
  while (i < f.length || j < g.length) {
    int nextX; // x следующей точки излома

    // Определяем следующую точку излома — минимум из f и g
    if (i >= f.length) {
      nextX = g[j].$1; // f закончился — берём из g
    } else if (j >= g.length) {
      nextX = f[i].$1; // g закончился — берём из f
    } else {
      nextX = f[i].$1 < g[j].$1 ? f[i].$1 : g[j].$1; // минимум
    }

    // Обновляем значения f и g если их следующая точка совпадает с nextX
    if (i < f.length && f[i].$1 == nextX) {
      fVal = f[i].$2; // f меняет значение в этой точке
      i++;
    }
    if (j < g.length && g[j].$1 == nextX) {
      gVal = g[j].$2; // g меняет значение в этой точке
      j++;
    }

    // Сумма в точке nextX
    final sum = fVal + gVal;

    // Добавляем точку если значение изменилось (убираем лишние дубликаты)
    if (result.isEmpty || result.last.$2 != sum) {
      result.add((nextX, sum));
    }
  }

  return result;
}
