// ============================================================
// ЗАДАЧА: Пересечение двух отсортированных массивов
// ============================================================
// Найти общие элементы двух отсортированных массивов.
// Каждый элемент в результате должен встречаться столько раз,
// сколько он встречается в обоих массивах одновременно.
//
// Примеры:
//   intersect([1,2,2,3], [2,2,4]) → [2,2]
//   intersect([1,3,5,7], [2,4,6]) → []
//   intersect([1,2,3], [1,2,3])   → [1,2,3]
// ============================================================

List<int> intersect(List<int> a, List<int> b) {
  final result = <int>[];

  // Два указателя — по одному на каждый массив
  int i = 0; // указатель по массиву a
  int j = 0; // указатель по массиву b

  // Двигаемся пока оба указателя в границах
  while (i < a.length && j < b.length) {
    if (a[i] == b[j]) {
      // Элементы совпали — добавляем в результат и двигаем оба указателя
      result.add(a[i]);
      i++;
      j++;
    } else if (a[i] < b[j]) {
      // a[i] меньше — он не может совпасть с b[j] или последующими
      // (b отсортирован, дальше только больше), двигаем указатель a
      i++;
    } else {
      // b[j] меньше — аналогично двигаем указатель b
      j++;
    }
  }

  return result;
}

// ============================================================
// ВАРИАНТ: Пересечение без учёта дубликатов (уникальные элементы)
// ============================================================

List<int> intersectUnique(List<int> a, List<int> b) {
  final result = <int>[];
  int i = 0;
  int j = 0;

  while (i < a.length && j < b.length) {
    if (a[i] == b[j]) {
      // Добавляем только если не дубликат в результате
      if (result.isEmpty || result.last != a[i]) {
        result.add(a[i]);
      }
      i++;
      j++;
    } else if (a[i] < b[j]) {
      i++;
    } else {
      j++;
    }
  }

  return result;
}
