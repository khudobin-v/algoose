// ============================================================
// РЕШЕНИЕ: Свёртка списка ID в интервалы
// ============================================================

String compressIds(List<int> ids) {
  // Пустой список — сразу возвращаем пустую строку
  if (ids.isEmpty) return '';

  // ШАГ 1: Сортируем копию (не меняем оригинал!)
  // После сортировки последовательные числа будут рядом
  final sorted = List<int>.from(ids)..sort();

  final result = <String>[];

  // start и end — границы текущего интервала
  // В начале оба указывают на первый элемент
  int start = sorted[0];
  int end = sorted[0];

  // ШАГ 2: Проходим с i=1, каждый раз смотрим на sorted[i]
  for (int i = 1; i < sorted.length; i++) {
    if (sorted[i] == end + 1) {
      // Следующий элемент продолжает интервал (идёт подряд)
      // Просто расширяем правую границу
      end = sorted[i];
    } else {
      // Разрыв — текущий интервал закончился
      // Записываем его и начинаем новый
      result.add(_fmt(start, end));
      start = sorted[i];
      end = sorted[i];
    }
  }

  // ШАГ 3: Не забыть записать ПОСЛЕДНИЙ интервал!
  // Он не записывается внутри цикла (цикл заканчивается до разрыва)
  result.add(_fmt(start, end));

  // Объединяем через запятую
  return result.join(',');
}

// Вспомогательная функция форматирования интервала
// Одиночный элемент: "5"
// Интервал: "3-7"
String _fmt(int start, int end) {
  return start == end ? '$start' : '$start-$end';
}
