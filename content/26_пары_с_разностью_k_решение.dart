// ============================================================
// ЗАДАЧА: Количество пар с разностью >= K
// ============================================================
// Дан массив чисел и число K. Найти количество пар (i,j), i<j,
// таких что |a[i] - a[j]| >= K.
//
// Примеры:
//   countPairs([1,3,7,2], 3) → 3  (пары: (1,7),(1,2... нет), (3,7),(1,7),(2,7))
//   countPairs([1,2,3,4], 2) → 3  (1-3, 1-4, 2-4)
//   countPairs([1,1,1,1], 1) → 0
// ============================================================

int countPairsWithDiff(List<int> a, int k) {
  if (a.length < 2) return 0;

  // Сортируем копию массива
  final sorted = List<int>.from(a)..sort();

  int count = 0;

  // Скользящее окно: для каждого левого указателя l
  // находим первый правый указатель r такой что sorted[r] - sorted[l] >= k
  // Все элементы правее r тоже образуют пары с sorted[l]

  int r = 1; // правый указатель начинается с 1

  for (int l = 0; l < sorted.length - 1; l++) {
    // Двигаем r вправо пока разность меньше k
    // r должен быть строго правее l
    if (r <= l) r = l + 1;

    while (r < sorted.length && sorted[r] - sorted[l] < k) {
      r++; // разность мала — двигаем правую границу
    }

    // Все элементы от r до конца дают разность >= k с sorted[l]
    // Их количество: sorted.length - r
    count += sorted.length - r;
  }

  return count;
}
