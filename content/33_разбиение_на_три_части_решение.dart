// ============================================================
// ЗАДАЧА: Разбиение массива на 3 части минимальной стоимости
// ============================================================
// Разбить массив на 3 непустых непрерывных подмассива.
// Стоимость подмассива = его первый элемент.
// Минимизировать суммарную стоимость (сумму первых элементов трёх частей).
//
// Примеры:
//   minCost([1,2,3,0,3]) → 1+2+0=3 (части [1],[2],[3,0,3]) нет...
//   → [1,2,3,0,3]: первый всегда a[0].
//   стоимость = a[0] + a[i] + a[j], где 1<=i, i<j, j<n
//   минимизировать a[0] + min(a[1..n-2]) + min(a[i+1..n-1])
// ============================================================

// Минимальная стоимость разбиения на 3 части
// Возвращает (суммарная стоимость, индексы разбиения)
(int cost, int split1, int split2) splitThreeParts(List<int> a) {
  final n = a.length;
  // Нужно минимум 3 элемента
  if (n < 3) throw ArgumentError('Array must have at least 3 elements');

  // Первый элемент всегда входит в стоимость (первый подмассив начинается с a[0])
  // Нам нужно выбрать split1 (начало 2-й части) и split2 (начало 3-й части)
  // split1 in [1..n-2], split2 in [split1+1..n-1]
  // Стоимость = a[0] + a[split1] + a[split2]
  // Минимизируем: ищем минимум a[split1] для всех split1 от 1 до n-2
  // и для каждого split1 ищем минимум a[split2] от split1+1 до n-1

  // Один проход: идём слева направо
  // min2 — минимум среди a[1..i-1] (лучший split1 до текущей позиции)
  // bestSplit1 — индекс этого минимума

  int min2 = a[1];      // лучший второй элемент
  int bestSplit1 = 1;   // позиция лучшего split1

  int bestCost = a[0] + a[1] + a[2]; // инициализация первым возможным разбиением
  int bestI = 1;
  int bestJ = 2;

  // Проходим по возможным значениям split2 от 2 до n-1
  for (int j = 2; j < n; j++) {
    // Для текущего j: лучший split1 — это min2 (минимум a[1..j-1])
    final cost = a[0] + min2 + a[j];

    if (cost < bestCost) {
      bestCost = cost;
      bestI = bestSplit1;
      bestJ = j;
    }

    // Обновляем min2 для следующей итерации: a[j] может стать новым split1
    if (a[j] < min2) {
      min2 = a[j];
      bestSplit1 = j; // запоминаем индекс для восстановления ответа
    }
  }

  return (bestCost, bestI, bestJ);
}
