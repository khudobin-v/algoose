// ============================================================
// ЗАДАЧА: Максимальная серия единиц с одной заменой
// ============================================================
// Дан бинарный массив. Можно перевернуть не более одного нуля.
// Найти максимальную длину непрерывной серии единиц после замены.
//
// Примеры:
//   maxConsecutiveOnes([1,0,1,1,0])   → 4
//   maxConsecutiveOnes([1,0,1,1,0,1]) → 4
//   maxConsecutiveOnes([1,1,1])        → 3
//   maxConsecutiveOnes([0,0,0])        → 1
// ============================================================

int maxConsecutiveOnes(List<int> nums) {
  int left = 0;   // левая граница окна
  int zeros = 0;  // количество нулей в текущем окне
  int maxLen = 0; // лучший результат

  for (int right = 0; right < nums.length; right++) {
    if (nums[right] == 0) zeros++;

    // Пока в окне больше одного нуля — сжимаем слева
    while (zeros > 1) {
      if (nums[left] == 0) zeros--;
      left++;
    }

    // Окно [left..right] содержит не более одного нуля
    final len = right - left + 1;
    if (len > maxLen) maxLen = len;
  }

  return maxLen;
}
