// ============================================================
// РЕШЕНИЕ: Максимальное число гостей в гостинице (Sweep Line)
// ============================================================

int maxGuests(List<(int, int)> intervals) {
  if (intervals.isEmpty) return 0;

  // ШАГ 1: Превращаем каждый интервал в два события
  // Заезд в момент T  → событие (T, +1)
  // Выезд в момент T  → событие (T, -1)
  final events = <(int, int)>[];

  for (final (from, to) in intervals) {
    events.add((from, 1));   // гость приехал
    events.add((to, -1));    // гость уехал
  }

  // ШАГ 2: Сортируем события по времени
  // Критически важно: при одинаковом времени выезд (-1) идёт РАНЬШЕ заезда (+1)
  // Почему? По условию: гость выезжает в T и новый заезжает в T → они НЕ пересекаются
  // Если сначала обработать заезд — счётчик ошибочно увеличится
  events.sort((a, b) {
    if (a.$1 != b.$1) return a.$1.compareTo(b.$1); // сначала по времени
    return a.$2.compareTo(b.$2); // при равном времени: -1 < +1, выезд раньше
  });

  // ШАГ 3: Проходим по событиям, отслеживаем текущее кол-во гостей
  int current = 0;
  int maxCount = 0;

  for (final (_, type) in events) {
    current += type; // +1 при заезде, -1 при выезде

    // Обновляем максимум ПОСЛЕ каждого заезда
    if (current > maxCount) maxCount = current;
  }

  return maxCount;
}
