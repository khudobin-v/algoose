// ============================================================
// ЗАДАЧА: Оптимизация пути — удаление петель
// ============================================================
// Дан путь из направлений L/R/U/D (Left/Right/Up/Down).
// Упростить путь, убрав петли (повторные посещения точек).
// Начальная позиция (0,0).
//
// Примеры:
//   optimizePath("LRUD") → "LRUD"  (нет петель, если вернулись в (0,0))
//   optimizePath("RDLU") → ""      (вернулись в начало — петля)
//   optimizePath("RRDLL") → "R"    (R→R→D→L→L: (0,0)→(1,0)→(2,0)→(2,-1)→(1,-1)→(0,-1))
// ============================================================

String optimizePath(String path) {
  // Храним: позиция → индекс первого посещения
  // Если посещаем позицию повторно — всё между первым и текущим посещением — петля
  final visited = <(int, int), int>{};

  // Текущая позиция
  int x = 0;
  int y = 0;

  // Список шагов (каждый шаг — направление)
  // Будем хранить явно для возможности "отрезать" петли
  final steps = <String>[];

  // Начальная позиция
  visited[(0, 0)] = 0;

  for (int i = 0; i < path.length; i++) {
    final dir = path[i];

    // Двигаемся в нужном направлении
    switch (dir) {
      case 'L': x--; break; // влево: x уменьшается
      case 'R': x++; break; // вправо: x увеличивается
      case 'U': y++; break; // вверх: y увеличивается
      case 'D': y--; break; // вниз: y уменьшается
    }

    // Добавляем шаг в список
    steps.add(dir);

    // Проверяем: были ли мы уже в этой точке?
    final pos = (x, y);
    if (visited.containsKey(pos)) {
      // Нашли петлю! Убираем все шаги с момента первого посещения этой точки
      final loopStart = visited[pos]!; // индекс в steps где была эта точка
      // Убираем шаги петли: от loopStart до конца
      steps.removeRange(loopStart, steps.length);
      // Также очищаем все позиции посещённые в петле из visited
      visited.removeWhere((key, value) => value >= loopStart);
      // Текущую позицию не нужно добавлять — она уже есть в visited[pos]
    } else {
      // Новая позиция — запоминаем
      visited[pos] = steps.length; // запоминаем текущий индекс в steps
    }
  }

  return steps.join();
}
