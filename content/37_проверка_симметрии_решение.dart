// ============================================================
// ЗАДАЧА: Проверка центральной симметрии набора точек
// ============================================================
// Проверить, является ли набор точек центрально-симметричным.
// Набор симметричен относительно центра (cx, cy) если для каждой
// точки (x, y) существует точка (2*cx-x, 2*cy-y).
//
// Центр симметрии вычисляется как среднее арифметическое всех точек.
//
// Примеры:
//   [(1,0),(0,1),(-1,0),(0,-1)] → true  (симметрично относительно (0,0))
//   [(0,0),(1,0),(2,0)]         → true  (центр (1,0))
//   [(0,0),(1,0),(2,1)]         → false
// ============================================================

bool isSymmetric(List<(int, int)> points) {
  if (points.isEmpty) return true; // пустое множество симметрично
  if (points.length % 2 != 0) return false; // нечётное кол-во — не симметрично

  // Вычисляем центр симметрии как среднее точек
  // Чтобы избежать дробей, работаем с удвоенными координатами
  // Центр (cx, cy) = (sum_x / n, sum_y / n)
  // Симметричная точка: (2*cx - x, 2*cy - y)
  // Умножаем на n: (2*sum_x/n - x) * n = 2*sum_x - n*x
  // Но проще: центр должен быть целым или полуцелым

  // Сумма координат должна быть чётной для целого центра
  int sumX = 0;
  int sumY = 0;
  for (final (x, y) in points) {
    sumX += x;
    sumY += y;
  }

  // Для n точек: центр = (sumX/n, sumY/n)
  // Удвоенный центр: (2*sumX/n, 2*sumY/n) = (doubleCX, doubleCY)
  // sumX * 2 должно делиться на n (иначе центр не целый и не полуцелый)
  final n = points.length;
  final doubleCX = 2 * sumX; // = n * 2cx
  final doubleCY = 2 * sumY; // = n * 2cy

  // Кладём все точки в HashSet для быстрой проверки
  final pointSet = <(int, int)>{};
  for (final p in points) {
    pointSet.add(p);
  }

  // Для каждой точки проверяем существование симметричной
  for (final (x, y) in points) {
    // Симметричная точка: (2*cx - x, 2*cy - y)
    // Умноженная на n: (doubleCX/n - x ... нет)
    // Если doubleCX = n * (2cx), то симм точка: (doubleCX/n - x)
    // Но целочисленность: проверяем что doubleCX делится на n
    if (doubleCX % n != 0 || doubleCY % n != 0) return false;

    final symmX = doubleCX ~/ n - x;
    final symmY = doubleCY ~/ n - y;

    if (!pointSet.contains((symmX, symmY))) return false;
  }

  return true;
}
