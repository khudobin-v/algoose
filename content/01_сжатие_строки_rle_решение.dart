// ============================================================
// РЕШЕНИЕ: RLE сжатие строки
// ============================================================

String rleEncode(String s) {
  // Защита от пустой строки — сразу бросаем ошибку
  if (s.isEmpty) throw ArgumentError('S is empty');

  // StringBuffer эффективнее чем конкатенация строк: O(n) vs O(n²)
  final res = StringBuffer();

  // Счётчик текущей группы. Начинаем с 1 — первый символ уже "виден"
  int count = 1;

  // Идём от 1 до length ВКЛЮЧИТЕЛЬНО (<=)
  // Зачем <= ? На итерации i==length мы попадём в else и запишем последнюю группу
  // Без этого последний символ/группа теряется
  for (int i = 1; i <= s.length; i++) {
    // i < s.length защищает от выхода за границу при s[i]
    // s[i] == s[i-1]: текущий символ продолжает группу → просто считаем
    if (i < s.length && s[i] == s[i - 1]) {
      count++;
    } else {
      // Группа закончилась (или это последняя итерация i==length)
      // Пишем символ — всегда s[i-1], потому что именно он завершил группу
      res.write(s[i - 1]);

      // Число пишем только если символов больше одного
      // "A" → "A", но "AAA" → "A3"
      if (count > 1) {
        res.write(count);
      }

      // Сбрасываем счётчик для следующей группы
      count = 1;
    }
  }

  return res.toString();
}

// ============================================================
// БОНУС: декодирование RLE обратно
// ============================================================

String rleDecode(String s) {
  final res = StringBuffer();
  int i = 0;

  while (i < s.length) {
    // Читаем букву
    final char = s[i];
    i++;

    // Читаем число (может быть многозначным: A12 → 12 раз)
    int count = 0;
    while (i < s.length &&
        s[i].codeUnitAt(0) >= 48 &&
        s[i].codeUnitAt(0) <= 57) {
      count = count * 10 + int.parse(s[i]);
      i++;
    }

    // Если числа не было — символ встречается 1 раз
    res.write(char * (count == 0 ? 1 : count));
  }

  return res.toString();
}
